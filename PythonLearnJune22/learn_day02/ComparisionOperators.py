var_one = 9
var_two = 2
# > < >= <= != ==
# Result of comparison is always boolean data type
print("greater than ", (var_one > var_two))
print("greater than or equals ", (var_one >= var_two))
print("less than ", (var_one < var_two))
print("less than or equals ", (var_one <= var_two))
print("not equal ", (var_one != var_two))
print("equal ", (var_one == var_two))
