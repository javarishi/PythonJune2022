import math

var_one = 9
var_two = 2

print("Addition " , (var_one + var_two))
print("Subtraction " , (var_one - var_two))
print("Multiplication " , (var_one * var_two))
print("Division " , (var_one / var_two))
print("Modulus " , (var_one % var_two))
print("Power of " , (var_one ** var_two))
print("Floor Division " , (var_one // var_two))
print("Factorial " , math.factorial(4))


